def area(a):
    """
    Рассчитывает площадь квадрата.
    
    :param a: Длина стороны квадрата
    :return: Площадь квадрата
    """
    return a * a


def perimeter(a):
    """
    Рассчитывает периметр квадрата.
    
    :param a: Длина стороны квадрата
    :return: Периметр квадрата
    """
    return 4 * a
