def area(a, b, c):
    """
    Вычисляет полупериметр треугольника.
    
    :param a: Длина стороны a.
    :param b: Длина стороны b.
    :param c: Длина стороны c.
    :return: Полупериметр треугольника (s).
    """
    return (a + b + c) / 2


def perimeter(a, b, c):
    """
    Вычисляет периметр треугольника.
    
    :param a: Длина стороны a.
    :param b: Длина стороны b.
    :param c: Длина стороны c.
    :return: Периметр треугольника (p).
    """
    return a + b + c
